#include "libft.h"

int		ft_ishexa(char c)
{
	if (ft_isdigit(c) == 1)
		return (1);
	else if (c > 64 && c < 71)
		return (1);
	else if (c > 96 && c < 103)
		return (1);
	else if (c == 'x')
		return (1);
	return (0);
}
